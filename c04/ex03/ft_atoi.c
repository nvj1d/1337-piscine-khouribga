int	ft_isspace(char c)
{
	if (c == ' ' || c == '\t'
		|| c == '\v' || c == '\r'
		|| c == '\n' || c == '\f')
		return (1);
	return (0);
}

int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	c;

	c = 1;
	i = 0;
	r = 0;
	while (ft_isspace(str[i]))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			c *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r *= 10;
		r += str[i] - '0';
		i++;
	}
	return (r *= c);
}
