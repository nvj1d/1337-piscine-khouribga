int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*str)
	{
		if (*str >= 32 && *str < 127)
		{
			i++;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (1);
}
