int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*str)
	{
		if (*str >= '0' && *str <= '9')
		{
			i++;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (1);
}
